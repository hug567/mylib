fn test_cmp_number() {
    println!("[test_cmp_number]: -------------------------------------");

    let mut num1:i32 = 5;
    let mut num2:i32 = 4;
    println!("num1: {}, num2: {}", num1, num2);

    num1 = 6;
    num2 = 7;
    println!("num1: {}, num2: {}", num1, num2);
}

fn main() {
    test_cmp_number();
}
